import java.util.*;

class Solution {
    public static void main(String[] args) {
        try (Scanner scan = new Scanner(System.in)) {
            int i;
            double d;
            String s;
            i = scan.nextInt();
            d = scan.nextDouble();
            s = scan.nextLine();
            s = scan.nextLine();
            System.out.println("String: " + s);
            System.out.println("Double: " + d);
            System.out.println("Int: " + i);
        }
    }
}
